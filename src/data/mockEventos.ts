// Função para gerar coordenadas próximas (variação de ~5km)
const gerarCoordenada = (baseLat: number, baseLon: number, offset: number = 0.05) => {
  return {
    latitude: baseLat + (Math.random() - 0.5) * offset,
    longitude: baseLon + (Math.random() - 0.5) * offset,
  };
};

